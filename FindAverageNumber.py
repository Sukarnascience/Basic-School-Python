# Write a Program to find average number of 5 subject

a = float(input("first subject marks:-"))
b = float(input("second subject marks:-"))
c = float(input("third subject marks:-"))
d = float(input("forth subject marks:-"))
e = float(input("fifth subject marks:-"))
f = (a+b+c+d+e)/5
print("the average of 5 Subject is ",f)
